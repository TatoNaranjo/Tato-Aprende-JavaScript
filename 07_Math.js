//Math Round: Redondear Un Numero
resultado = Math.round(2.5);

//Math Ceil: Siempre Redondea Un Numero Hacia Arriba
resultado = Math.ceil(2.2);

//Math Floor: Siempre Redondea Un Numero Hacia Abajo.
resultado = Math.ceil(2.9);

